//A C Program to Check a person is old or Young
#include <stdio.h>
#include <stdlib.h>

int main()
{
    int Age;
    printf("Enter the Age of The Person: ");
    scanf("%d",&Age);
    if(Age>=50)
        {printf("The Person is Old");}
    else
       {printf("\nThe Person is Young");}
    return 0;
}
