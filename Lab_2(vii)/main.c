//C program to convert killo ohm to ohms.
#include <stdio.h>
#include <stdlib.h>

int main()
{
    float r;
    printf("Enter the value of resistor (in Killo ohms): ");
    scanf("%f",&r);
    printf("The value of resistor is %.1f ohms",r*1000);
    return 0;
}
