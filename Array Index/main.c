// A C program to work with Array Indexes.
#include <stdio.h>

void main ()
{
double salary[] = {10000.00,15000.00,20500.50 ,
5050.50,30000.00};
/* Let ’s print all salary one by one */
int i;
for (i =0;i<5;i++)
    {
    printf (" Salary [%d]: %.2lf \n", i, salary [i]);
    }
}
