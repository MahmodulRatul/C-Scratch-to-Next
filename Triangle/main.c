//A C program to find the area of a triangle
#include <stdio.h>
#include <stdlib.h>

void main()
{
    float Area , Base , Height ;
    printf ("Input Base   :");
    scanf("%f",&Base);
    printf("Input Height :");
    scanf("%f",&Height);
    Area =(Base*Height)/2;
    printf("The Area of The Triangle is : %.2f",Area);
}
